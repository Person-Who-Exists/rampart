<h3>Submitting a WAD</h3>

<p>The easiest way to submit a WAD to RAMP is through the <a href="./upload.php">uploader</a> on this site - you can use the PIN given to you after the initial upload to submit further edits to your WAD once it's been submitted. However, if you have a WAD that doesn't fit through the uploader, you can get in touch with David through the Doomworld thread or on Discord. This video shows how to use the uploader.</p>

