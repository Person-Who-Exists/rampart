<p>There are a ton of guides for editing Doom out on Youtube - here are a few resources to get you started!</p>

<p><a href="https://eev.ee/blog/2015/12/19/you-should-make-a-doom-level-part-1/">Eevee's guide (focuses on mapping with Slade3)</a></p>
<p><a href="https://eev.ee/blog/2015/12/19/you-should-make-a-doom-level-part-1/">Linguica's guide (older but still good)</a></p>

<h3>You will need</h3>

<ul>
<li>Either a copy of <a href="https://www.gog.com/game/doom_ii_final_doom">DOOM2.WAD</a> or <a href="https://freedoom.github.io/download.html">FREEDOOM2.WAD from Freedoom</a></li>
<li>A source port for running Doom levels (e.g. <a href="http://prboom.sourceforge.net/">PrBoom</a>, <a href="https://zdoom.org/index">GZDoom</a>)</li>
<li>A DOOM level editor (e.g. <a href="https://forum.zdoom.org/viewtopic.php?f=232&t=66745">Ultimate Doom Builder</a>, <a href="https://slade.mancubus.net/">Slade3</a>)</li>
<li>(Optional, to get some extra textures to work with) <a href="https://www.wad-archive.com/wad/195ea6c6a1a0b0f70d066bb3125dd1c342858d99">cc4-tex.wad</a></li>
</ul>

<p>I've also collected most of these into <a href="./rampstarterpack.zip">an editing starter pack for Windows, which you can download here</a>. See below for instructions on setting yourself up!</p>

